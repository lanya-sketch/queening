/**
 * 응답 형식 지시.
 * 군주 대화와 같은 구분자 방식 — 스트리밍 중 JSON 이 화면에 새지 않게 한다.
 * 이 화면에서 움직일 수 있는 값은 해당 캐릭터의 호감도와 심신뿐이다.
 */
export function characterFormat(charId: string): string {
  return [
    '응답 형식:',
    '1) 먼저 이 인물의 말과 행동만 쓴다. 3~5문장. 지문은 최소한으로.',
    '2) 그 다음 줄에 <<<META>>> 라고 쓰고, 그 아래 한 줄로 JSON 을 쓴다.',
    `   {"deltas":[{"target":"affection:${charId}","amount":1}]}`,
    `   target 은 affection:${charId} 와 wellbeing 만 쓸 수 있다.`,
    '   amount 는 -3 에서 3 사이 정수. 변화가 없으면 {"deltas":[]} 라고 쓴다.',
    '3) <<<META>>> 앞의 내용만 상대에게 보인다. JSON 을 대사 안에 섞지 않는다.',
  ].join('\n')
}
